const findKey = (object, callback) => {
  const listOfObjects = Object.keys(object).filter((key) =>
    callback(object[key])
  );
  return listOfObjects[0];
};

findKey(
  {
    "Blue Hill": { stars: 1 },
    Akaleri: { stars: 3 },
    noma: { stars: 2 },
    elBulli: { stars: 3 },
    Ora: { stars: 2 },
    Akelarre: { stars: 3 },
  },
  (x) => x.stars === 2
); // => "noma"

